function renderSeparators() {
  [...$('divider')].map((divider) =>
    $(divider).replaceWith(`<div class="sep">
      <h4 class="sep-title">${$(divider).attr('data-title')}</h4>
      <div class="sep-line"></div>
    </div>`)
  );
}

function getContacts() {
  function getContact(title, value, icon) {
    return `<div class="contact">
          <img
            class="contact-icon"
            src="${icon}"
            alt="${title}"
          />
          <span class="contact-value">${value}</span>
        </div>`;
  }
  return cv.contactInfo
    .map(({ title, value, icon }) => getContact(title, value, icon))
    .join('\n');
}

function getSkills() {
  function getSkill(title, skills) {
    return `<div>
          <b>${title}</b>
          <ul>
          ${skills.map((skill) => `<li>${skill}</li>`).join('\n')}
          </ul>
        </div>`;
  }
  return cv.skills
    .map(({ title, skills }) => getSkill(title, skills))
    .join('\n');
}

